DROP TABLE IF EXISTS G05_contribuer;
DROP TABLE IF EXISTS G05_Serie CASCADE;
DROP TABLE IF EXISTS G05_Album;
DROP TABLE IF EXISTS G05_Auteur;

-- Création de la table Serie
CREATE TABLE G05_Serie(
	serie_id INTEGER PRIMARY KEY,
	serie_titre VARCHAR(80) NOT NULL,
	dateDeCreation VARCHAR(50) NOT NULL 
	);

-- Création de la table Album
CREATE TABLE G05_Album(
	album_id INTEGER PRIMARY KEY,
	album_titre VARCHAR(80) NOT NULL,
	dateSortie VARCHAR(50) NOT NULL,
	serie_id INTEGER NOT NULL,
	FOREIGN KEY (serie_id)  REFERENCES G05_Serie(serie_id)
	);

-- Création de la table Auteur
CREATE TABLE G05_Auteur(
	auteur_id SERIAL PRIMARY KEY,
	auteur_nom VARCHAR(80) NOT NULL,
	auteur_prenom VARCHAR(80) NOT NULL
	);

-- Création de la table contribuer
CREATE TABLE G05_contribuer(
	auteur_fonction VARCHAR(80) CHECK (auteur_fonction IN('Dessinateur' , 'Scénariste' , 'Coloriste')),
	auteur_id INT NOT NULL,
	FOREIGN KEY (auteur_id)  REFERENCES G05_Auteur(auteur_id),
	album_id INT NOT NULL,
	FOREIGN KEY (album_id)  REFERENCES G05_Album(album_id),
	PRIMARY KEY (auteur_id , album_id)
	);
	
